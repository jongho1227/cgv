CREATE SCHEMA cgv;

-- 회원
CREATE TABLE cgv.member (
	mb_id            VARCHAR(20) NOT NULL COMMENT '아이디', -- 아이디
	mb_password      CHAR(41)    NOT NULL COMMENT '비밀번호', -- 비밀번호
	mb_name          VARCHAR(10) NOT NULL COMMENT '이름', -- 이름
	mb_birth         DATETIME    NOT NULL COMMENT '생년월일', -- 생년월일
	mb_phone         VARCHAR(15) NOT NULL COMMENT '전화번호', -- 전화번호
	mb_email         VARCHAR(50) NOT NULL COMMENT '이메일', -- 이메일
	mb_join_date     DATETIME    NOT NULL DEFAULT now() COMMENT '가입일', -- 가입일
	mb_withdrawal    TINYINT     NOT NULL DEFAULT false COMMENT '탈퇴여부', -- 탈퇴여부
	mb_grade         VARCHAR(10) NULL     COMMENT '등급', -- 등급
	mb_mileage       INT         NULL     COMMENT '마일리지', -- 마일리지
	mb_pass_question VARCHAR(50) NULL     COMMENT '비밀번호질문', -- 비밀번호질문
	mb_pass_answer   VARCHAR(50) NULL     COMMENT '비밀번호답' -- 비밀번호답
)
COMMENT '회원';

-- 회원
ALTER TABLE cgv.member
	ADD CONSTRAINT PK_member -- 회원 기본키
		PRIMARY KEY (
			mb_id -- 아이디
		);

